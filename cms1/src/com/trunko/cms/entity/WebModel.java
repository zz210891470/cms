package com.trunko.cms.entity;


/**
 * 系统模型类，数据库设计中没有，根据前台页面编写
 * @author gjz
 *
 */
public class WebModel {

	private Long id;
	private String mxbt;//模型标题
	private int mxpx;  //模型排序
	private String xtmr;//是否系统默认
	private String mxurl;//模板url位置
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getMxbt() {
		return mxbt;
	}
	public void setMxbt(String mxbt) {
		this.mxbt = mxbt;
	}
	public int getMxpx() {
		return mxpx;
	}
	public void setMxpx(int mxpx) {
		this.mxpx = mxpx;
	}
	public String getXtmr() {
		return xtmr;
	}
	public void setXtmr(String xtmr) {
		this.xtmr = xtmr;
	}
	public void setMxurl(String mxurl) {
		this.mxurl = mxurl;
	}
	public String getMxurl() {
		return mxurl;
	}

	
	
}
